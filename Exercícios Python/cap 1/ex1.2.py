numero_A = int(input("Digite o primeiro número: "))
numero_B = int(input("Digite o primeiro número: "))

soma = numero_A + numero_B
subtracao = numero_A - numero_B
multiplicacao = numero_A * numero_B
duvisao = numero_A / numero_B

print(f"A soma de {numero_A} e {numero_B} é igual a {soma}.")
print(f"A subtracao de {numero_A} e {numero_B} é igual a {subtracao}.")
print(f"A multiplicacao de {numero_A} e {numero_B} é igual a {multiplicacao}.")
print(f"A divisao de {numero_A} e {numero_B} é igual a {duvisao}.")